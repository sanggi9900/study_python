# 관계연산자 : 값들간의 관계를 비교한다.
# - 이 연산자가 사용된 연산식을 조건식이라 부름
# - 같은 종류의 값끼리만 비교할 수 있다.
# - 다른 종류일 경우, 일치/불일치만 체크할 수 있음
display_size = 11.6
print(display_size < 12) # 미만 / 같은 것을
print(display_size > 12) # 초과 / 허용 안함
notebook_weight = 1
print(notebook_weight >= 1) # 이상 / 같은 것을
print(notebook_weight <= 1) # 이하 / 포함한다
pc_ram = 16
print(pc_ram == 16) # 일치 : 같아야 한다.
print(pc_ram != 16) # 불일치 : 달라야 한다.