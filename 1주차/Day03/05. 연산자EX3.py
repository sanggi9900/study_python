# 입력과 형변환의 응용 - 입력받은 것을 바로 바꾼다.
value1 = int(input("첫번째 정수 입력 >> "))
value2 = int(input("두번째 정수 입력 >> "))
# print 거치면 값의 종류 관계없이 무조건 나옵니다.
print(value1, "+", value2, "=", value1 + value2)
print(value1, "-", value2, "=", value1 - value2)
print(value1, "*", value2, "=", value1 * value2)
print(value1, "/", value2, "=", value1 / value2)