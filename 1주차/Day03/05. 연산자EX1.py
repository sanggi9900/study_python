value1 = 100
value2 = 3.14
# 변수를 사용할 때는 철저하게 사용하자.
print(value1, "+",  value2, "=", value1 +  value2)
print(value1, "-",  value2, "=", value1 -  value2)
print(value1, "x",  value2, "=", value1 *  value2)
print(value1, "/",  value2, "=", value1 /  value2)
print(value1, "//", value2, "=", value1 // value2)
print(value1, "%",  value2, "=", value1 %  value2)
print(value1, "**", value2, "=", value1 ** value2)

# 값의 범위
# 정수 : 파이썬에서는 경단위이기 때문에 엄청난 대수를 저장가능
# 실수 : 소수점 15자리. 이를 넘어서는 결과는 잘라버린다.
