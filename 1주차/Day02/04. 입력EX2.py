# 요령 - 입력은 나중에 추가하면 된다.

# -- 입력구간 --
value1 = input("값1 >> ")
value2 = input("값2 >> ")
value3 = input("값3 >> ")

# -- 처리구간 --
# 내일 우리가 배우면 입력과 출력 중간에 추가됩니다.
# 여기서는 자료를 용도에 맞게 가공하게 됩니다.

# -- 출력구간 --
print(value1, value3, value2)
print(value1, value1, value3)
print(value3, value2, value1)
# 코드작성요령은 기술이고, 기술은 배우면 쓸 수 있다.
# 프로그램은 정답이 없다.
# 정답이라고 생각할려면, 의도를 담아야 한다.
print(value1, value1, value2, value2)
print(value1, value1, value1, value2)
print(value1, value2, value2, value1)