# print() 팁
# - 괄호안에 아무것도 안넣으면 줄바꿈만 됩니다.
# 상수를 쓴다 - 1회성 자료 - 특정 종류가 아니면 문자값으로 모두 커버된다.
print("상수출력")
print("<홍길동> <35> <코딩>")

# 변수를 쓴다 - 핵심자료만 저장시켜줘야 된다.
# - 자료형은 현실에서 어떻게 쓰이고 있는지를 떠올리며 정한다.
# - ex) 나이 -> 평균을 낼 수 있다. -> 문자가 아닌 숫자여야 한다.
name = "홍길동"
age = 35
hobby = "코딩"
print("변수출력")
print("<",name,"> <",age,"> <",hobby,">")
# -> 여러번 쓴다고 변수가 아님. 저장해놓고 쓸 "가치"가 있는 것인가?